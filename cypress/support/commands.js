// This command will prevent cypress from opening a new window
Cypress.Commands.add("staySameWindow", () => {
  cy.window().then((win) => {
    const orig = win.open;
    win.open = function (url, target, features) {
      return orig.call(this, url, "_self", features);
    };
  });
});
